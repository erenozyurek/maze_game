def find_value(val, current_maze):
    rows = len(current_maze)
    cols = len(current_maze[0])
    for y in range(rows):
        for x in range(cols):
            if current_maze[y][x] == val:
                return x, y
    return None

def get_map(map_num):
    return [row[:] for row in maps[map_num]]
